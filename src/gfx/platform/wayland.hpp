// Wayland-client backend. Loads libwayland-client.so.0 and libwayland-egl.so.1
// via micron::dso and exposes the entry points the display + surface code
// will call.
//
// Window creation under Wayland flows through the xdg-shell protocol, which
// is NOT in libwayland-client — it's a separate XML protocol whose
// marshalling is normally generated by `wayland-scanner` from xdg-shell.xml.
// We hand-roll that marshalling in gl/platform/wayland_xdg_shell.hpp.

